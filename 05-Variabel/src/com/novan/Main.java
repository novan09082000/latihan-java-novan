package com.novan;

public class Main {
    public static void main(String[] args){
        // variabel adalah wadah untuk diisi dengan nilai
        // a merupakan variabel
        // 10 merupakan nilai
        // int adalah tipe data
        int a = 10;
        System.out.println("nilai a = " + a);

        // variabel bisa diisi/diassigment ulang nilai sebelumnya
        a = 20;
        System.out.println("nilai a sekarang = " + a);

        // deklarasi adalah perjanjian atau menentukan bahwa variabel apa dan tipe data termasuk apa
        int b; // contoh deklarasi

        // assigment merupaka perintah untuh memasukan nilai kedalam variabel
        b = 7; // contoh assigment

        // namun dalam kasus lain kita bisa deklarasi sambil assigment nilai variabel nya
        // contoh => int c = 10;

        System.out.println("nilai b = " + b);
    }
}
